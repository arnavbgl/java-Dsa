package patterns;

public class pat1 {
    public static void main(String[] args) {
        int n =8;
        for (int i =0; i<n;i++){
            int j=0;
            while(j<n){
                System.out.print("* ");
                j++;
            }
            System.out.println();
        }
    }
}
